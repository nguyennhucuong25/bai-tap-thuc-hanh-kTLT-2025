## My script using the math module ## 
import mymath  # Note no .py 
values = [2,4,6,8,10] 
print('Squares:') 
# Hàm squares tính bình phương
for v in values: 
    print(mymath.square(v)) 
print ('Cubes:')
# Hàm cubes tính lập phương 
for v in values: 
    print(mymath.cube(v)) 
print('Average: ' + str(mymath.average(values)))
